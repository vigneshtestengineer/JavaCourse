package JavaLearn;

public class Whilecondition {

	public static void main(String[] args) {
		// Print the numbers from 1 to 10 number using while
		
		int i = 10;
		
		 while (i<=10) {
			 
			 System.out.println(i);
			 i++;
		 }
		 
		 
		 int j = 10;
		 while (j>0) {
			 
			 System.out.println(j);
			 j--;
		 }

	}

}
